/**
 * Asigna im치genes por defecto sin repetir (orden secuencial).
 * Cada convocatoria sin imagen obtiene una de la lista en orden.
 */
export function asignarImagenesPorDefecto(convocatorias: any[]) {
  let index = 0;

  return convocatorias.map((conv) => {
    if (!conv.imageUrl || conv.imageUrl.trim() === "") {
      const seleccionada = imagenesPorDefecto[index % imagenesPorDefecto.length];
      index++;
      return { ...conv, imageUrl: seleccionada };
    }
    return conv;
  });
}
